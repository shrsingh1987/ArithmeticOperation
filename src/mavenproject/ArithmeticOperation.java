package mavenproject;
public class ArithmeticOperation {

	public int add(int a,int b)
	{
		int c = a+b;
		return c;
	}
	public int subtract(int a, int b)
	{
		int c = a-b;
		return c;
	}
	public int multiply(int a, int b)
	{
		int c = a*b;
		return c;
	}
	public int divide(int a, int b)
	{
		int c = a/b;
		return c;
	}
	public int modus(int a, int b)
	{
		int c = a%b;
		return c;
	}
}
